angka1 = int(input("Masukkan bilangan pertama ="))
angka2 = int(input("Masukkan bilangan kedua ="))

hasil1 = angka1 + angka2
hasil2 = angka1 - angka2
hasil3 = angka1 * angka2
hasil4 = angka1 / angka2
hasil5 = angka1 % angka2
hasil6 = angka1 ** angka2

print(f"\nPenjumlahan {angka1} + {angka2} \t= \t{hasil1} \nPengurangan {angka1} - {angka2} \t= \t{hasil2} \nPerkalian {angka1} x {angka2} \t= \t{hasil3} \nPembagian {angka1} : {angka2} \t= \t{hasil4} \nSisa bagi {angka1} % {angka2} \t= \t{hasil5} \nPerpangkatan {angka1} ^ {angka2} \t= \t{hasil6}")